from numpy.random import randint as rnd
from time import strftime 
from sys import exit

# SYSTEM / MODULE #########################################

class system():

    def __init__(self, name):
        date = strftime("%A, %d %b %Y at %H:%M:%S")
        print(f"create system {name}\n{date}")
        self.devicelist = {}
        self.name = name
        self.date = date
        self.time = 0
        # done
        return

    # time units are in ns (float) 
    def addClock( 
        self,
        period = 20,    # 20ns, 50MHz
        width  = 10,    # symetrical clock
        phase  = 0,     # 0ns, in phase
        name   = None): # None, generic name

        # get generic clock name
        if not name:
            # find a new name
            n, k = 0, "clk0"
            while k in self.devicelist.keys():
                n += 1
                k = f"clk{n}"
            # found name
            name = k

        # create
        self.devicelist[name] = \
            clock(period, width, phase, name)

        # done
        return self.devicelist[name]

    # time units are in ns (float) 
    def addCounter( 
        self,
        size = 4,     # 4, half a byte
        name = None): # None, generic name

        # get generic counter name
        if not name:
            # find a new name
            n, k = 0, "cnt0"
            while k in self.devicelist.keys():
                n += 1
                k = f"cnt{n}"
            # found name
            name = k

        # create
        self.devicelist[name] = \
            counter(size, name)

        # done
        return self.devicelist[name]

    def displayDevices(self):
        for d in self.devicelist.values(): d.display()            
        return

    def openFile(self, pathName = "./output.vcd"):
        # create file
        fh = open(pathName, 'w')
        # make header
        fh.write(f"$version Generated by simsys.py $end\n")
        fh.write(f"$date {self.date} $end\n")
        # fh.write(f"$timezero 0 $end\n")
        fh.write(f"$timescale 1ns $end\n")
        # make modules and signals
        n = 0
        fh.write(f"$scope module SYSTEM $end\n")
        for d in self.devicelist.values():
            n = d.makeModule(fh, n, 0)
        fh.write(f"$upscope $end\n")
        # close header
        fh.write(f"$enddefinitions $end\n")
        # set initial values at time zero
        fh.write(f"#0\n")
        for d in self.devicelist.values():
            fh.write(d.export())
        # done
        self.pathName = pathName
        self.fh = fh
        return

    def closeFile(self):
        self.fh.close()
        return

    def updateDevices(self):
        # increase time interval in units of 1ns
        self.time += 1
        # update device inputs
        for d in self.devicelist.values():
            d.updateInputPorts()        
        # update device outputs
        change = ""        
        for d in self.devicelist.values():
            change += d.updateOutputPorts(self.time)
        # export
        if change:
            self.fh.write(f"#{self.time}\n")
            self.fh.write(change)
        return

    def runUntil(self, time):
        while self.time < time:
            self.updateDevices()
        return

# OUTPUT PORTS ############################################

class outPort():

    def __init__(self, bits = 1):
        # so far, default is undefined value 'U'
        self.value = 'U'*bits
        # self.signal = None
        return

    def get(self):
        return self.value

    def set(self, value):
        # check type is string 
        if not isinstance(value, str):
            print(f"port.set: value must be of string type.")
            print(f"  exiting...")
            exit()
        # check size
        if not len(value) == len(self.value):
            print(f"port.set: value size mismatch:")
            print(f"  value size is {len(value)}.")
            print(f"  expected size is {len(self.value)}.")
            print(f"  exiting...")
            exit()
        # update
        self.value = value
        # done
        return

    def addSignal(self, n):
        self.signal = f"O{n}"
        return self.signal

    # export to VCD format
    def export(self):
        if self.signal:
            # export bit string
            if self.size() > 1:
                return f"b{self.value} {self.signal}\n"
            # export single bit
            return f"{self.value}{self.signal}\n"
        # no signal
        return ""

    def size(self):
        return len(self.value)

# INPUT PORTS #############################################

class inPort():

    def __init__(self, port):
        # linking (network)
        self.p = port
        # initial state
        self.state = port.get()
        # self.rising  = False
        # self.falling = False
        return

    def update(self):
        if self.p.size() > 1:
            self.state = self.p.get()
            return
        # single bit transition        
        s, p = self.state, self.p.get()
        # clear previous edges
        self.rising  = False
        self.falling = False
        # detect rising edge
        if (s, p) == ('0','1'):
            self.rising = True                 
        # detect falling edge
        if (s, p) == ('1','0'):
            self.falling = True
        # update state
        self.state = p                 
        return

    def addSignal(self, n):
        self.signal = f"I{n}"
        return self.signal

