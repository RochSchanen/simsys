# wrt on active low, high
# ena on active low, high
# cse on active low, high
# initial value random, zero, maybe undefined
# maybe parallel, serial load

class counter():

    # activated when input connected
    clr = None # clear   (reset)
    trg = None # trigger (clock)
    wrt = None # write   (load)
    ena = None # enable  (output)
    cse = None # chip select

    def __init__(
        self,
        size,   # counter bits width 
        name):  # counter name
        # record data
        self.name = name
        self.configuration = size
        self.Q = outPort(size)
        # setup port with a random value
        self.Q.set(f'{rnd(size):0{size}b}')
        return

    def makeModule(self, fh, n, t):
        # get data
        name = self.name
        size = self.configuration
        # make tab for alignment
        tab  = '\t'*(t+1)
        # make signal label
        label  = f"{name}_Q[{size-1}:0]"
        # register port for export (VCD)
        signal = self.Q.addSignal(n)
        # write module
        fh.write(f"{tab}$scope module {name} $end\n")
        fh.write(f"{tab}\t$var wire {size} {signal} {label} $end\n")
        fh.write(f"{tab}$upscope $end\n")
        return n+1

    def export(self):
        return self.Q.export() 

    def display(self):
        # get data
        name = self.name
        size = self.configuration
        value = self.Q.get()
        # display
        print(f"CNT: {name},{size},{value}")
        return

    def addTrigger(self, port):
        self.trg = inPort(port)
        return

    def addClear(self, port):
        self.clr = inPort(port)
        return

    def updateInputPorts(self):
        for p in [
            self.clr,   # clear 
            self.trg]:  # trigger
            if p: p.update()
        return

    def updateOutputPorts(self, timeStamp):

        # get configuration
        size = self.configuration

        # asynchronous clear on active low
        if self.clr.state == '0':
            # make zero string
            zero = f'{0:0{size}b}'
            # no change
            if self.Q.get() == zero: return ""
            # clear ouputs
            self.Q.set(zero)
            # export
            return self.export()

        # update on rising edge of trigger
        if self.trg.rising:
            # increment current value
            n = int(self.Q.get(), 2) + 1
            # make n string, LSB(size) only
            ns = f'{n:0{size}b}'[-size:]
            # update outputs
            self.Q.set(ns)
            # export
            return self.export()

        # no change
        return ""

# CLOCK ###################################################

class clock():

    def __init__(
        self,
        period, # clock period (float time)
        width,  # pulse width  (float time)
        phase,  # phase shift  (float time)
        name):  # clock name
        # record data
        self.name = name
        self.configuration = period, width, phase
        self.Q = outPort(1)
        # done
        return

    def makeModule(self, fh, n, t):
        # get data
        name = self.name
        # make tab for alignment
        tab  = '\t'*(t+1)
        # make signal label
        label = f"{name}_Q"
        # register port for export (VCD)
        signal = self.Q.addSignal(n)
        # write module content
        fh.write(f"{tab}$scope module {name} $end\n")
        fh.write(f"{tab}\t$var wire 1 {signal} {label} $end\n")
        fh.write(f"{tab}$upscope $end\n")
        return n+1

    def export(self):
        return self.Q.export()

    def display(self):
        # get data
        name = self.name
        period, width, phase = self.configuration
        value = self.Q.get()
        # display
        print(f"CLK: {name},{period},{width},{phase},{value}")
        return

    def updateInputPorts(self):
        pass

    def updateOutputPorts(self, timeStamp):
        # get configuration
        period, width, phase = self.configuration
        # get new state
        m = (timeStamp-phase) % period
        s = ['0','1'][m < width]
        # no change
        if self.Q.get() == s: return ""
        # update output
        self.Q.set(s)        
        # export
        return self.export()

